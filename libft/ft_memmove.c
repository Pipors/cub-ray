#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (src == dest)
		return (dest);
	if (dest < src)
	{
		while (i < n)
		{
			((char *)dest)[i] = ((char *)src)[i];
			i++;
		}
	}
	else
	{
		while (i < n)
		{
			((char *)dest)[i + n - 1] = ((char *)src)[i + n - 1];
			n--;
		}
	}
	return (dest);
}
